// This code will explain a simple sum
#include <iostream> //Include iostream library for printing text
using namespace std;

int main() {
    // Step 1: Declare Variables to hold the two input numbers and their sum 
    int num1 = 8; // First integer variable, initialized with the value 8
    int num2 = 10; // second integer variable, initialize with the value 10.

    int sum = num1 + num2; // Third integer variable, score the sum.

    cout << "The sum of the 8 and 10 is: " << sum << endl; // Prin the result of the sum.

    return 0; // Program finish successfully.

}